// operadores de atribuicao
void main() {
  int a = 2;
  a = a + 3;
  a += 3; // acrescenta mais 3 ao valor da variavel a
  a = 3; //troca o valor de a
  a -= 2;
  a *= 5;
  print(a);

  //operadores relacionais- binarios e fix
  //resultado sempre booleano

  print(3 > 6);
  print(4 < 7);
  print(4 != 7);
  print(4 == 7);
}
